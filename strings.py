print('String basics')
s = 'hi' #assign a variable to a string
print(s[1]) #prints the second varaiable in the string (python starts at 0)
print(len(s)) #this returns the length of the string
print(s + ' how are you?') #returns the string plus another string
#all of these use the variable we assigned the the string
print('---------------------------------')

print('str method')
mynum = 736 #integer assigned to a variable
print(mynum) #this will print the value as an integer
print(str(mynum)) #this will print the variable as a string
print('---------------------------------')

print('other methods')
st = 'lol'
print(st.upper()) #returns the string in uppercase, make sure to use the variable name
print(st.lower()) #returns the string in lowercase
print(st.replace('l','w')) #replaces instances of l with w in the string



